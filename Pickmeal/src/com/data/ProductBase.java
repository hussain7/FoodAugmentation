package com.data;

import java.io.Serializable;

/**
 * this class represent the base of a product
 */
public class ProductBase implements Serializable {
	private static final long serialVersionUID=8266388580004027475L;
	protected int id;
	protected String name;
	public ProductBase(int id,String name) {
		this.id=id;
		this.name=name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id=id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name=name;
	}
	@Override
	public String toString() {
		return name;
	}
}
